#include "libft.h"

char			**ft_colchr(char **tab, int c)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (i < 4)
	{
		if (tab[i][j] == c)
			return (&tab[i]);
		i++;
	}
	return (0);
}
